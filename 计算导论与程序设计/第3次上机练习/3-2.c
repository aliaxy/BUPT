/*
3-2 实验2_9_数列求和

已知正整数n与数列公式:
An = 1*2*3+2*3*4+3*4*5+…+(n-2)*(n-1)*n (n>=3)
An = 0 (n=1,2)

请根据n值计算该数列的前n项和的值,并输出。n的取值范围是(1<=n<=3000)。

注意：这里的An 只是一项，而我们要求的是计算该数列的前n项和。

提示：由于n值很大，建议使用long long型变量存储结果。

输入格式:
只有一个正整数n(1<=n<=3000)。

输出格式:
只有一个整数，为计算结果。

输入样例:
3000
输出样例:
12149993250000600
*/
#include <stdio.h>

int main(void)
{
    int n;
    long long An = 0, p, Sn = 0, x = 3;

    scanf("%d", &n);

    if (n >= 1 && n <= 2)
        Sn = 0;
    else
        while (x <= n)
        {
            p = x * (x - 1) * (x - 2);
            An = An + p;
            Sn = Sn + An;
            x++;
        }

    printf("%lld", Sn);
    return 0;
}